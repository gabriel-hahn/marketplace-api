const mongoose = require('mongoose')

const PurchaseSchema = new mongoose.Schema({
  ad: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Ad',
    required: true
  },
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  content: {
    type: String,
    required: true
  },
  closed: {
    type: Boolean,
    required: true,
    default: false
  }
})

module.exports = mongoose.model('Purchase', PurchaseSchema)
